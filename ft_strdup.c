#include "libft.h"

char	*ft_strdup(char *src)
{
	int		len;
	int		i;
	char	*tmp;

	len = ft_strlen(src);
	tmp = malloc(sizeof(char) * (len + 1));
	if (!(tmp))
		return (NULL);
	i = 0;
	while (src[i] != '\0')
	{
		tmp[i] = src[i];
		i++;
	}
	tmp[i] = '\0';
	return (tmp);
}
